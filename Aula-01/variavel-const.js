//Variáveis const
/* Cada variável const mantém seu valor inicial e não pode ser reatribuída posteriormente. Portanto, os valores atribuídos a essas variáveis permanecem constantes ao longo da execução do programa. */

//tipo de valores aceitos em const
const PI = 3.14159; // Número constante
const nome = "Walter Netto"; // String constante
const isAtivo = true; // Booleano constante
const pessoa = { // Objeto literal constante
  nome: "Renata",
  idade: 30
};
const numeros = [1, 2, 3, 4, 5]; // Array constante

console.log(PI); // Saída: 3.14159
console.log(nome); // Saída: Walter Netto
console.log(isAtivo); // Saída: true
console.log(pessoa); // Saída: { nome: 'Renata', idade: 30 }
console.log(numeros); // Saída: [ 1, 2, 3, 4, 5 ]


