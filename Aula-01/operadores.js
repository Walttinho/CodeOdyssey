let a = 5;
let b = 3;

// Adição
//A adição (+) é usada para somar a e b e armazenar o resultado na variável soma.
let soma = a + b; // Soma: 5 + 3 = 8

// Subtração
//A subtração (-) é usada para subtrair b de a e armazenar o resultado na variável subtração.
let subtracao = a - b; // Subtração: 5 - 3 = 2

// Multiplicação
//A multiplicação (*) é usada para multiplicar a por b e armazenar o resultado na variável multiplicação.
let multiplicacao = a * b; // Multiplicação: 5 * 3 = 15

// Divisão
//A divisão (/) é usada para dividir a por b e armazenar o resultado na variável divisão.
let divisao = a / b; // Divisão: 5 / 3 = 1.6666666666666667

// Resto da Divisão
//O resto da divisão (%) é usado para obter o resto da divisão de a por b e armazenar o resultado na variável resto.
let resto = a % b; // Resto da Divisão: 5 % 3 = 2

// Incremento
//O operador de incremento (++) é usado para incrementar o valor de a em 1.
a++; // Incremento: a é incrementado em 1 (agora a é igual a 6)

// Decremento
//O operador de decremento (--) é usado para decrementar o valor de b em 1.
b--; // Decremento: b é decrementado em 1 (agora b é igual a 2)


const msg = 'O resultado da operação de'

// Impressão dos resultados
console.log(msg+' '+`Soma é: ${soma}`);
console.log(msg+' '+`Subtração é: ${subtracao}`);
console.log(msg+' '+`Multiplicação é: ${multiplicacao}`);
console.log(msg+' '+`Divisão é: ${divisao}`);
console.log(msg+' '+`Resto da Divisão é: ${resto}`);
console.log(msg+' '+`Incremento de 'a' é : ${a}`);
console.log(msg+' '+`Decremento de 'b' é: ${b}`);
